#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;

	if (!s1 || !s2)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i])
	{
		i++;
	}
	while ((s1[i] = s2[j]))
	{
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
