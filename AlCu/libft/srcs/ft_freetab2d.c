#include "libft.h"

void		ft_freetab2d(char **t)
{
	int		i;

	if (!t)
		return ;
	i = 0;
	while (t[i])
	{
		ft_memdel((void**)&t[i]);
		i++;
	}
	ft_memdel((void**)&t);
}
