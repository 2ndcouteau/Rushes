#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t i;
	size_t e;

	i = 0;
	e = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[e] != '\0')
	{
		s1[i] = s2[e];
		e++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
