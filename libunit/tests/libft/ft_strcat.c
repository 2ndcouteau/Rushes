#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		ls1;
	int		i;

	ls1 = ft_strlen(s1);
	i = 0;
	while (s2[i] != '\0')
	{
		s1[i + ls1] = s2[i];
		i++;
	}
	s1[i + ls1] = '\0';
	return (s1);
}
